#ifndef LLVM_LIB_TARGET_LANAI_LANAISUBTARGET_H
#define LLVM_LIB_TARGET_LANAI_LANAISUBTARGET_H

#include "EtcaFrameLowering.h"
#include "EtcaISelLowering.h"
#include "EtcaInstrInfo.h"
#include "EtcaSelectionDAGInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "EtcaGenSubtargetInfo.inc"

namespace llvm {

class EtcaSubtarget : public EtcaGenSubtargetInfo {
public:
  // This constructor initializes the data members to match that
  // of the specified triple.
  EtcaSubtarget(const Triple &TargetTriple, StringRef Cpu,
                 StringRef FeatureString, const TargetMachine &TM,
                 const TargetOptions &Options, CodeModel::Model CodeModel,
                 CodeGenOptLevel OptLevel);

  // ParseSubtargetFeatures - Parses features string setting specified
  // subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  EtcaSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  void initSubtargetFeatures(StringRef CPU, StringRef FS);

  bool enableMachineScheduler() const override { return true; }

  const EtcaInstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const EtcaRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  const EtcaTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const EtcaSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

private:
  EtcaFrameLowering FrameLowering;
  EtcaInstrInfo InstrInfo;
  EtcaTargetLowering TLInfo;
  EtcaSelectionDAGInfo TSInfo;
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_LANAI_LANAISUBTARGET_H
